class BookFormatter{
    constructor(
        book,
        user,
        words,
        columns,
        font,
        font_size,
        color
    ){
        this.book = book;
        this.user = user;
        this.words = words;
        this.columns = columns;
        this.font = font;
        this.font_size = font_size;
        this.color = color;
        
    }
}